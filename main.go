package main

import "fmt"

func main() {

	//ints
	var ageOne int = 20
	var ageTwo = 30
	ageThree := 40

	fmt.Println(ageOne, ageTwo, ageThree)

	//bits & memory
	var nameOne int8 = 25
	var nameTwo int8 = -128
	var nameThree uint8 = 25

	fmt.Println(nameOne, nameTwo,  nameThree)
	
	//floats
	var scoreOne float32 = -1.5
	var scoreTwo float32 = 25.98
	scoreThree := 1.5

	fmt.Println(scoreOne, scoreTwo,  scoreThree)

}
